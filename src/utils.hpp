template <typename T> static void printVec(std::vector<T> &inp) {
    std::cout << std::setprecision(10);
    for (auto val : inp) {
        std::cout << val << ", ";
    }
    std::cout << std::endl << std::endl;
}

template <typename T> static void printVec(Vec2d<T> &inp) {
    std::cout << std::setprecision(10);
    for (auto row : inp) {
        for (auto val : row) {
            std::cout << val << ", ";
        }
        std::cout << std::endl;
    }
    std::cout << std::endl << std::endl;
}

